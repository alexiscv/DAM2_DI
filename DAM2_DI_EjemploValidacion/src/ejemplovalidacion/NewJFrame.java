    private boolean validarFormulario(){
        
        String nombre = jTextFieldNombre.getText();
        String edad = jTextFieldEdad.getText();
        
        if( nombre == null || "".equals(nombre) ){
            JOptionPane.showMessageDialog(this, "Nombre no puede ser vacio", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
            
        }
        
        // Como la edad es un entero
        // Parseamos el contenido del String a Int
        // Si da error, significa que el contenido no era un n√∫mero
        try{
            Integer.parseInt(jTextFieldEdad.getText());
            
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "El campo edad tiene que ser un entero","Error",JOptionPane.ERROR_MESSAGE);
            return false;
        }
               
        return true;
        
    }
    
